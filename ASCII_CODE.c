#include <stdio.h>

/*A programme to show the ASCII code corresponding to each alphabet in English*/
int main () {
	int main () {
	// show all ASCII codes alongside its value
	
	int n = 0;
	while (n <= 255) {
		printf("The ASCII value of %c is %d\n", n, n);
		n++;
	}
	return 0;
}
	
}
